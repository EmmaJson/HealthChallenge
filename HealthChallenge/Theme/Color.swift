import Foundation
import SwiftUI

extension Color {
    static let theme = ColorTheme()
}

struct ColorTheme {
    let accent = Color.accentColor
    let background = Color.background
    let onBackground = Color.onBackground
    let primaryText = Color.primaryText
    let secondaryText = Color.secondaryText
    let colorBlue = Color.colorBlue
    let colorRed = Color.colorRed
    let colorGreen = Color.colorGreen
    let colorYellow = Color.colorYellow
    let colorPink = Color.colorPink
    let colorTeal = Color.colorTeal
}
